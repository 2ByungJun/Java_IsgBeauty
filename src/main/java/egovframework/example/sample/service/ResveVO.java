package egovframework.example.sample.service;

/**
 * ResveVO
 * @author 이병준
 *
 */
public class ResveVO extends SampleDefaultVO {

	private static final long serialVersionUID = 1L;

	private String resveSn;
	private String mberSn;
	private String resveDt;
	private String resveTime;
	private String tretmentNm;
	private String processSttus;
	private String registId;
	private String registDt;
	private String mberNm;
	
	
	public String getResveSn() {
		return resveSn;
	}
	public void setResveSn(String resveSn) {
		this.resveSn = resveSn;
	}
	public String getMberSn() {
		return mberSn;
	}
	public void setMberSn(String mberSn) {
		this.mberSn = mberSn;
	}
	public String getResveDt() {
		return resveDt;
	}
	public void setResveDt(String resveDt) {
		this.resveDt = resveDt;
	}
	public String getResveTime() {
		return resveTime;
	}
	public void setResveTime(String resveTime) {
		this.resveTime = resveTime;
	}
	public String getTretmentNm() {
		return tretmentNm;
	}
	public void setTretmentNm(String tretmentNm) {
		this.tretmentNm = tretmentNm;
	}
	public String getProcessSttus() {
		return processSttus;
	}
	public void setProcessSttus(String processSttus) {
		this.processSttus = processSttus;
	}
	public String getRegistId() {
		return registId;
	}
	public void setRegistId(String registId) {
		this.registId = registId;
	}
	public String getRegistDt() {
		return registDt;
	}
	public void setRegistDt(String registDt) {
		this.registDt = registDt;
	}
	public String getMberNm() {
		return mberNm;
	}
	public void setMberNm(String mberNm) {
		this.mberNm = mberNm;
	}
	
	
	
}


